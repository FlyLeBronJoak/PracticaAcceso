    private void Escribe(String wrote, String ruta) {
        try{
            //se crea el buffered writer, le decimos la ruta para guardarlo y lo que queremos escribir
            BufferedWriter bw = new BufferedWriter(new FileWriter(ruta));
            bw.write(wrote);

            bw.close();

        }catch (Exception e) {
            System.out.println("fallo al escribir archivo");
        }
    }

    private void Modifica(String wrote, String ruta) {
        try{
            //igual que el anterior bufferedwriter, pero ahora creamos una nueva linea para modificar
            BufferedWriter bw = new BufferedWriter(new FileWriter(ruta, true));
            bw.newLine();
            bw.write(wrote);

            bw.close();

        }catch (Exception e) {
            System.out.println("fallo al modificar el archivo");
        }
    }

    private void Leer(String ruta) throws FileNotFoundException {
        try{
            //se crea el buffered reader, le damos la ruta del archivo que queremos abrir 
            //y donde queremos que escriba su contenido
            BufferedReader br = new BufferedReader(new FileReader(ruta));
            jTextArea1.read(br,br);

            br.close();

        }catch (IOException e) {
            System.out.println("fallo al leer el archivo");
        }

    }

