    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        //se crea el FileChooser
        JFileChooser jFC = new JFileChooser();

        try{
            if(jFC.showOpenDialog(null) == jFC.APPROVE_OPTION) {

                //elegimos la ruta del archivo para que lo lea el bufferedreader
                String rutaArchivo = jFC.getSelectedFile().getAbsolutePath();
                Leer(rutaArchivo);
            }
        }catch (Exception ex) {
            System.out.println("fallo boton1");
        }
    }//GEN-LAST:event_jButton1MouseClicked

